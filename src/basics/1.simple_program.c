/*
 * ============================================================================
 * C程序结构说明：
 *
 * 一个C程序通常由一个或多个源文件组成，每个源文件包含：
 *
 * 1. 预处理指令 (Preprocessor Directives)
 *    - 以 # 开头，如 #include <stdio.h>
 *    - 在编译前由预处理器处理
 *    - 用于包含头文件、宏定义等
 *
 * 2. 全局声明 (Global Declarations)
 *    - 在函数外部声明的变量和函数
 *    - 在整个程序中可见（全局作用域）
 *
 * 3. 函数定义 (Function Definitions)
 *    - 包含main函数和其他自定义函数
 *    - main函数是程序执行的入口点
 * ============================================================================
 */

// 标准输入输出头文件
// stdio = Standard Input Output
// .h = Header File (头文件)
#include <stdio.h>

/*
 * ============================================================================
 * main函数 - 程序主入口
 *
 * 函数原型: int main(void) 或 int main(int argc, char *argv[])
 * 返回值: int类型，通常返回0表示成功，非0表示错误
 *
 * 说明:
 * - 每个C程序必须有一个main函数
 * - 程序从main函数开始执行
 * - main函数结束标志程序终止
 * ============================================================================
 */
int main(void)
{
    // 使用printf函数输出字符串到标准输出（通常是屏幕）
    // printf = print formatted (格式化输出)
    printf("Hello, World!\n");

    // 可以输出更多信息
    printf("Welcome to C Programming!\n");
    printf("This is a simple demonstration.\n");

    /*
     * return语句的作用：
     * 1. 结束main函数的执行
     * 2. 向操作系统返回一个整数值
     * 3. 0通常表示程序正常结束
     * 4. 非0值通常表示错误或异常状态
     */
    return 0;  // 返回0表示程序成功执行
}

/*
 * ============================================================================
 * 详细解释：
 *
 * 1. #include <stdio.h>
 *    - stdio.h 包含输入输出函数的声明
 *    - 如 printf(), scanf(), getchar(), putchar() 等
 *    - 没有这个头文件，printf函数无法使用
 *
 * 2. int main(void)
 *    - int: 函数返回类型，表示main函数返回一个整数
 *    - main: 函数名，程序入口点
 *    - void: 参数列表为空（也可以写空括号()）
 *
 * 3. printf("Hello, World!\n");
 *    - 将双引号内的字符串输出到屏幕
 *    - \n 是转义字符，表示换行（New Line）
 *    - 其他常用转义字符：
 *        \t 制表符
 *        \\ 反斜杠
 *        \" 双引号
 *        \' 单引号
 *
 * 4. return 0;
 *    - 在main函数中，return 0表示程序正常退出
 *    - 在Unix/Linux系统中，可以用 echo $? 查看返回值
 *
 * 5. 注释类型：
 *    - 单行注释: // 注释内容
 *    - 多行注释: /* 注释内容 *\/
 *
 * 6. 程序执行流程：
 *    源代码 → 预处理器 → 编译器 → 汇编器 → 链接器 → 可执行文件
 * ============================================================================
 */

